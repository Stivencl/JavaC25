public class Clase8 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Valor de i: " + i);
        }

        int i = 1;
        do {
            System.out.println("Valor de i: " + i);
            i++;
        } while (i <= 5);

        i = 1;
        while (i <= 5) {
            System.out.println("Valor de i: " + i);
            i++;
        }
    }


}


